//---展开运算符（...）
//与不定参数相似
//不定参数：可以指定多个各自独立的参数，并通过整合后的数组来访问
//展开运算符：可以指定一个数组，将它们打散后作为各自独立的参数传入函数
let values = [20, 50, 75, 100];
console.log('[展开运算符]---apply ' + Math.max.apply(Math, values)); //100
console.log('[展开运算符]---... ' + Math.max(...values)); //100
//等价于
console.log('[展开运算符]--- ' + Math.max(20, 50, 75, 100)); //100
//可以将展开运算符与其他正常传入的参数混合使用
console.log('[展开运算符]--- ' + Math.max(...values, 125)); //125